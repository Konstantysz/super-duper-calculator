import math

#two number operations
def add(num1,num2):
    return num1+num2 

def subtract(num1, num2):
    return num1-num2 

def multiply(num1,num2):
    return num1*num2

def devide(num1,num2):
    return num1/num2 

def remainder(num1,num2):
    return num1%num2  

def exponent(num1,num2):
    return num1**num2 

#one input operation
def square(num):
    return num*num

def inverse(num):
    return 1/num

def squrt(num):
    return num**0.5